def run_console(clicker):
    """Run console input loop."""
    while clicker.running:
        try:
            cmd = input("\n>>> ").strip().lower()

            if cmd in ("start", "s"):
                clicker.start()
            elif cmd in ("stop", "p"):
                clicker.stop()
            elif cmd in ("toggle", "t"):
                clicker.toggle()
            elif cmd in ("stats", "i"):
                stats = clicker.get_stats()
                print(f"\n  Trạng thái:   {'🟢 ON' if stats['scanning'] else '🔴 OFF'}")
                print(f"  Số lần retry: {stats['retry_count']}")
                print(f"  Uptime:       {stats['uptime']}")
                print(f"  Click cuối:   {stats['last_click']}")
            elif cmd in ("quit", "q", "exit"):
                clicker.stop()
                clicker.running = False
                print("\n👋 Tạm biệt!")
                break
            elif cmd == "help":
                print("\n  start/s  - Bắt đầu quét")
                print("  stop/p   - Dừng quét")
                print("  toggle/t - Bật/Tắt")
                print("  stats/i  - Thống kê")
                print("  quit/q   - Thoát")
            elif cmd:
                print("  ❓ Lệnh không hợp lệ. Gõ 'help' để xem danh sách lệnh.")

        except (KeyboardInterrupt, EOFError):
            clicker.stop()
            clicker.running = False
            print("\n👋 Tạm biệt!")
            break


