class AntigravityAutoRetry:
    """Main auto-retry clicker class."""

    def __init__(self):
        self.config = load_config()
        self.logger = setup_logger(self.config["log_file"])
        self.running = False
        self.scanning = False
        self.retry_count = 0
        self.session_start = None
        self.last_click_time = None
        self._stop_event = threading.Event()
        self._scan_thread = None

        # Validate button images exist
        self.button_images = []
        for img_path in self.config["button_images"]:
            full_path = os.path.join(SCRIPT_DIR, img_path)
            if os.path.exists(full_path):
                self.button_images.append(full_path)
                self.logger.info(f"✅ Loaded image: {img_path}")
            else:
                self.logger.warning(f"⚠️  Image not found: {full_path}")

        if not self.button_images:
            self.logger.error(
                "❌ Không tìm thấy ảnh nút Retry! "
                "Chạy 'python capture_button.py' để chụp ảnh nút trước."
            )

    def play_sound(self, sound_type="click"):
        """Play notification sound."""
        if not self.config.get("sound_enabled", True):
            return
            
        if HAS_WINSOUND:
            try:
                if sound_type == "click":
                    winsound.Beep(1000, 200)
                elif sound_type == "start":
                    winsound.Beep(800, 150)
                    winsound.Beep(1200, 150)
                elif sound_type == "stop":
                    winsound.Beep(1200, 150)
                    winsound.Beep(800, 150)
            except Exception:
                pass
        elif sys.platform == "darwin":
            # Native macOS sounds using afplay
            try:
                if sound_type == "click":
                    os.system("afplay /System/Library/Sounds/Ping.aiff &")
                elif sound_type == "start":
                    os.system("afplay /System/Library/Sounds/Glass.aiff &")
                elif sound_type == "stop":
                    os.system("afplay /System/Library/Sounds/Basso.aiff &")
            except Exception:
                pass

    def find_retry_button(self):
        """Tìm nút Retry trên màn hình."""
        confidence = self.config["confidence"]

        for img_path in self.button_images:
            try:
                location = pyautogui.locateCenterOnScreen(
                    img_path, confidence=confidence
                )
                if location:
                    self.logger.debug(
                        f"Found button at ({location.x}, {location.y}) "
                        f"using {os.path.basename(img_path)}"
                    )
                    return location
            except pyautogui.ImageNotFoundException:
                continue
            except Exception as e:
                self.logger.debug(f"Error scanning with {img_path}: {e}")
                continue

        return None

    def click_retry(self, location):
        """Click nút Retry tại vị trí đã tìm."""
        delay = self.config["click_delay_seconds"]

        self.logger.info(
            f"🎯 Phát hiện nút Retry tại ({location.x}, {location.y})!"
        )

        # Đợi một chút trước khi click (đề phòng dialog chưa ổn định)
        time.sleep(delay)

        # Verify lại vị trí (dialog có thể đã biến mất)
        verify_location = self.find_retry_button()
        if verify_location is None:
            self.logger.info("ℹ️  Dialog đã biến mất trước khi click, bỏ qua.")
            return False

        # Click!
        pyautogui.click(verify_location.x, verify_location.y)

        self.retry_count += 1
        self.last_click_time = datetime.now()

        self.logger.info(
            f"🔄 Đã click Retry! (Lần thứ {self.retry_count} trong session)"
        )
        self.play_sound("click")

        return True

    def scan_loop(self):
        """Main scanning loop."""
        interval = self.config["scan_interval_seconds"]
        max_retries = self.config["max_retries_per_session"]

        self.logger.info(f"🔍 Bắt đầu quét (mỗi {interval}s)...")

        while not self._stop_event.is_set():
            # Check max retries
            if max_retries > 0 and self.retry_count >= max_retries:
                self.logger.info(
                    f"⏹️  Đã đạt giới hạn {max_retries} lần retry. Dừng quét."
                )
                self.scanning = False
                break

            # Scan for retry button
            try:
                location = self.find_retry_button()
                if location:
                    self.click_retry(location)

                    # Đợi thêm sau khi click để Antigravity xử lý
                    cooldown = max(5, interval * 2)
                    self.logger.info(
                        f"⏳ Cooldown {cooldown}s sau khi click..."
                    )
                    self._stop_event.wait(cooldown)
                else:
                    self.logger.debug("Không tìm thấy nút Retry.")
            except Exception as e:
                self.logger.error(f"❌ Lỗi khi quét: {e}")

            # Wait for next scan
            self._stop_event.wait(interval)

        self.logger.info("⏹️  Đã dừng quét.")

    def start(self):
        """Start scanning."""
        if self.scanning:
            self.logger.info("ℹ️  Đang quét rồi!")
            return

        if not self.button_images:
            self.logger.error(
                "❌ Không có ảnh nút Retry! Chạy 'python capture_button.py' trước."
            )
            return

        self.scanning = True
        self.running = True
        self.session_start = datetime.now()
        self._stop_event.clear()

        self._scan_thread = threading.Thread(target=self.scan_loop, daemon=True)
        self._scan_thread.start()

        self.play_sound("start")
        self.logger.info("▶️  Auto-Retry đã bật!")

    def stop(self):
        """Stop scanning."""
        if not self.scanning:
            self.logger.info("ℹ️  Chưa quét!")
            return

        self.scanning = False
        self._stop_event.set()

        if self._scan_thread:
            self._scan_thread.join(timeout=5)

        self.play_sound("stop")
        self.logger.info(
            f"⏹️  Auto-Retry đã tắt. Tổng retry: {self.retry_count}"
        )

    def toggle(self):
        """Toggle scanning on/off."""
        if self.scanning:
            self.stop()
        else:
            self.start()

    def get_stats(self):
        """Get statistics."""
        uptime = ""
        if self.session_start:
            delta = datetime.now() - self.session_start
            hours, remainder = divmod(int(delta.total_seconds()), 3600)
            minutes, seconds = divmod(remainder, 60)
            uptime = f"{hours:02d}:{minutes:02d}:{seconds:02d}"

        return {
            "scanning": self.scanning,
            "retry_count": self.retry_count,
            "session_start": (
                self.session_start.strftime("%H:%M:%S") if self.session_start else "N/A"
            ),
            "uptime": uptime or "N/A",
            "last_click": (
                self.last_click_time.strftime("%H:%M:%S")
                if self.last_click_time
                else "N/A"
            ),
        }


