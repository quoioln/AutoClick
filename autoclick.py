def create_tray_icon(clicker):
    """Create system tray icon (optional)."""
    try:
        import pystray
        from PIL import Image, ImageDraw, ImageFont

        def create_icon_image(active=False):
            """Create a simple tray icon."""
            size = 64
            img = Image.new("RGBA", (size, size), (0, 0, 0, 0))
            draw = ImageDraw.Draw(img)

            # Background circle
            bg_color = (76, 175, 80, 255) if active else (158, 158, 158, 255)
            draw.ellipse([4, 4, size - 4, size - 4], fill=bg_color)

            # "R" letter
            try:
                font = ImageFont.truetype("arial.ttf", 32)
            except Exception:
                font = ImageFont.load_default()

            draw.text(
                (size // 2, size // 2),
                "R",
                fill=(255, 255, 255, 255),
                font=font,
                anchor="mm",
            )
            return img

        def on_toggle(icon, item):
            clicker.toggle()
            icon.icon = create_icon_image(clicker.scanning)
            icon.title = f"AutoRetry - {'ON' if clicker.scanning else 'OFF'}"

        def on_stats(icon, item):
            stats = clicker.get_stats()
            print("\n" + "=" * 40)
            print("  📊 THỐNG KÊ")
            print("=" * 40)
            print(f"  Trạng thái:  {'🟢 Đang quét' if stats['scanning'] else '🔴 Đã dừng'}")
            print(f"  Số lần retry: {stats['retry_count']}")
            print(f"  Bắt đầu:     {stats['session_start']}")
            print(f"  Uptime:       {stats['uptime']}")
            print(f"  Click cuối:   {stats['last_click']}")
            print("=" * 40 + "\n")

        def on_quit(icon, item):
            clicker.stop()
            icon.stop()

        menu = pystray.Menu(
            pystray.MenuItem(
                lambda item: f"{'⏹ Dừng' if clicker.scanning else '▶ Bắt đầu'}",
                on_toggle,
                default=True,
            ),
            pystray.MenuItem("📊 Thống kê", on_stats),
            pystray.Menu.SEPARATOR,
            pystray.MenuItem("❌ Thoát", on_quit),
        )

        icon = pystray.Icon(
            "AutoRetry",
            create_icon_image(False),
            "AutoRetry - OFF",
            menu,
        )

        return icon

    except ImportError:
        return None


